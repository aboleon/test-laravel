    {
        $flags      = self::getCountryFlags();
        $codes      = self::getCountryCallingCodes();
        $names      = Countries::getCode2Name();
        $selectable = [];
        foreach ($names as $code => $name) {
            $selectable[$code] = $name." ".$flags[$code]." ".$codes[$code];
        }

        return $selectable;
    }

